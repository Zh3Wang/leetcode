func minSubArrayLen(target int, nums []int) int {
	var left, right, sum int
	var ans = math.MaxInt

	for right < len(nums) {
		sum += nums[right]
		for sum >= target {
			ans = min(right-left+1, ans)
			sum -= nums[left]
			left++
		}
		right++
	}

	if ans == math.MaxInt {
		return 0
	}

	return ans
}

func min(a, b int) int {
	if a < b {
		return a
	}
	return b
}

